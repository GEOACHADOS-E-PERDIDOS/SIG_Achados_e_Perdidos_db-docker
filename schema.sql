CREATE TABLE Posto_retirada (
  ID_posto INTEGER PRIMARY KEY,
  nome VARCHAR(50),
  endereco VARCHAR(150),
  telefone VARCHAR(11),
  email VARCHAR(50),
  geom GEOMETRY(Point,4326)
);


CREATE TABLE Usuarios (
  ID_User INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  Name VARCHAR(50),
  email VARCHAR(50),
  Senha_hash VARCHAR(50),
  data_cadastro DATE,
  is_admin BOOLEAN,
  senha_temporaria BOOLEAN DEFAULT FALSE
);

CREATE TABLE Imagem_objeto (
  ID_imagem INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  caminho_imagem VARCHAR(50),
);

CREATE TABLE Objeto (
  ID_objeto INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome VARCHAR(50),
  descricao VARCHAR(50),
  endereco_encontro VARCHAR(150),
  data_encontro DATE,
  geom GEOMETRY(Point,4326),
  Posto_retirada_ID_posto INTEGER,
  Imagem_objeto_ID_imagem INTEGER,

  CONSTRAINT fk_Objeto_ref_Posto_retirada
    FOREIGN KEY (Posto_retirada_ID_posto)
    REFERENCES Posto_retirada(ID_posto),

  CONSTRAINT fk_Objeto_ref_Imagem_objeto
    FOREIGN KEY (Imagem_objeto_ID_imagem)
    REFERENCES Imagem_objeto(ID_imagem)
);

CREATE TABLE Categoria (
  ID_categoria INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  nome VARCHAR(50),
  descricao VARCHAR(150)
);

CREATE TABLE Objeto_Categoria (
  ID_objeto INTEGER,
  ID_categoria INTEGER,

  CONSTRAINT pk_Objeto_Categoria PRIMARY KEY (ID_objeto, ID_categoria),

  CONSTRAINT fk_objeto_categoria_objeto
    FOREIGN KEY (ID_objeto) REFERENCES Objeto(ID_objeto),

  CONSTRAINT fk_objeto_categoria_categoria
    FOREIGN KEY (ID_categoria) REFERENCES Categoria(ID_categoria)
);